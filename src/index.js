const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
    ' ': ' ',
};

function decode(expr) {
    let newArr =[]
    let result = []
    for(let i = 0; i< expr.length; i+=10) {
        newArr.push(expr.slice(i,i+10))
    }
    let sos = newArr.map(val => val.replaceAll('11', '-').replaceAll('10', '.').replaceAll('**********', ' ').replaceAll('0', ''))
    sos.map(val => {
        result.push(MORSE_TABLE[val]) 
    })  
    return result.join('')
}

module.exports = {
    decode
}